var Contract = HonorContract.at("0xb6bc01d5ad6b215fd9327514bf9edfcbc8465dea");

$('#template-contactform').on('submit', function(){

    var name = $('#template-contactform-name').val();
    var genres_array = $('.switch .switch-click').parent().prev('h3').text().trim().split(' ');
    var genres_array_bytes = [];

    for (var i = 0; i < genres_array.length; i++) genres_array_bytes.push(web3.fromAscii(genres_array[i], 32));

    Contract.registrate(name, [genres_array_bytes], transactionObject, (error, result) => {
        if(!error) console.log('Welcome to HonorCoin! You become one of us.');
        else console.log(error);
    });
    return false;
});

const transactionObject = {
    from: web3.eth.defaultAccount,
    gasPrice: 50000000000
};